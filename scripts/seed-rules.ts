async function main() {
    await prisma.settings.upsert({
        where: { key: 'rules_text' },
        update: { value: DEFAULT_RULES },
        create: { key: 'rules_text', value: DEFAULT_RULES },
    });
    console.log('Rules seeded successfully');
}

main()
    .catch((e) => {
        console.error(e);
        process.exit(1);
    })
    .finally(async () => {
        await prisma.$disconnect();
    });
